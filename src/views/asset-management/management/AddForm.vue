<template>
  <a-modal
    :title="modalTitle"
    :width="550"
    :visible="visible"
    :destroyOnClose="true"
    @ok="handleSubmit"
    @cancel="handleCancel"
    :confirmLoading="spinning"
    :maskClosable="false"
  >
    <a-spin :spinning="spinning">
      <a-form :form="form" class="wdf-custom-add-form-wrapper">
        <a-form-item label="资产类别">
          <a-select
            placeholder="选择资产类型"
            v-decorator="[
              'typeDicId',
              { initialValue: record.typeDicId, rules: [{ required: true, message: '选择资产类型' }] },
            ]"
            :allowClear="true"
            style="width: 100%"
          >
            <a-select-option v-for="item in assetTypeList" :key="item.id" :value="item.id">{{
              item.text
            }}</a-select-option>
          </a-select>
        </a-form-item>
        <a-form-item label="资产编码">
          <a-input
            style="width: 100%"
            v-decorator="[
              'code',
              {
                initialValue: record.code,
                rules: [
                  { required: true, message: '请输入资产编码' },
                  {
                    validator: changeKey,
                  },
                ],
              },
            ]"
          />
        </a-form-item>
        <a-form-item label="资产名称">
          <a-input
            style="width: 100%"
            v-decorator="[
              'name',
              { initialValue: record.name, rules: [{ required: true, message: '请输入资产名称' }] },
            ]"
          />
        </a-form-item>

        <a-form-item label="资产归属">
          <a-select
            placeholder="选择资产归属"
            v-decorator="[
              'beyondType',
              { initialValue: record.beyondType || 3, rules: [{ required: true, message: '选择资产归属' }] },
            ]"
            :allowClear="true"
            style="width: 100%"
          >
            <a-select-option :value="1">个人</a-select-option>
            <a-select-option :value="2">部门</a-select-option>
            <a-select-option :value="3">资产库</a-select-option>
          </a-select>
        </a-form-item>

        <a-form-item label="购买时间">
          <a-date-picker
            v-decorator="[
              'buyTime',
              {
                initialValue: record.buyTime ? moment(record.buyTime) : undefined,
                rules: [{ required: true, message: '请选择购买时间' }],
              },
            ]"
            style="width: 100%"
          />
        </a-form-item>
      </a-form>
    </a-spin>
  </a-modal>
</template>
<script>
import {
  departmentList, //所有部门
  getStationList, //获取所有岗位
} from '@/api/systemSetting'
import { getDictionaryList } from '@/api/workBox'
import { oaAssertsInfoAssertsAdd } from '@/api/assetManagement'
import moment from 'moment'
export default {
  name: 'asset-management-management-AddForm',
  data() {
    return {
      form: this.$form.createForm(this, { name: 'asset-management-management-AddForm' }),
      visible: false,
      spinning: false,
      type: 'view',
      assetTypeList: [],
      record: {},
      isManagerCheck: false,
    }
  },
  computed: {
    modalTitle() {
      return this.type === 'add' ? '新增' : '编辑'
    },
    isAdd() {
      return this.type === 'add'
    },
    isEdit() {
      return this.type === 'edit'
    },
  },
  methods: {
    async query(type, record) {
      let that = this
      that.visible = true
      that.type = type
      that.record = Object.assign({}, record)

      await that.initData()
      if (that.isEdit) {
        that.$nextTick(() => {
          that.form.setFieldsValue(Object.assign({}, that.record))
        })
      }
    },
    changeKey(rule, value, callback) {
      //定义你需要的正则
      let rgx = /[\u4E00-\u9FA5]/g //
      if (!rgx.test(value)) {
        //验证通过调用回调函数
        callback()
      } else {
        callback(new Error('禁止填写中文'))
      }
    },
    initData() {
      let that = this
      let queue = []
      let task1 = getDictionaryList({ parentId: 532 }).then((res) => (that.assetTypeList = res.data))
      queue.push(task1)
      return Promise.all(queue)
    },
    handleSubmit() {
      let that = this
      this.form.validateFields((err, values) => {
        if (!err) {
          console.log('Received values of form: ', values)
          if (that.isEdit) {
            values.id = that.record.id
          }
          if (values.buyTime && values.buyTime instanceof moment) {
            values.buyTime = values.buyTime.format('YYYY-MM-DD')
          }
          that.spinning = true
          oaAssertsInfoAssertsAdd(values)
            .then((res) => {
              that.spinning = false
              if (res.code === 200) {
                that.visible = false
                that.$message.success('操作成功')
                that.$emit('finish')
              } else {
                that.$message.warning(res.msg)
              }
            })
            .catch((err) => {
              that.spinning = false
              that.$message.error('操作失败')
            })
        }
      })
    },
    handleCancel() {
      this.form.resetFields()
      this.$nextTick(() => (this.visible = false))
    },
  },
}
</script>
